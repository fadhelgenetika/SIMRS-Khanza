    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if(TNoRw.getText().trim().equals("")||TPasien.getText().trim().equals("")){
            Valid.textKosong(TNoRw,"pasien");
        }else if(kddok.getText().trim().equals("")||namadokter.getText().trim().equals("")){
            Valid.textKosong(kddok,"Dokter P.J");
        }else if(cmbJenis.getSelectedItem().equals("-")){
            Valid.textKosong(cmbJenis,"Jenis");
        }else if(TPemeriksaan.getText().trim().equals("")){
            Valid.textKosong(TPemeriksaan,"Pemeriksaan");
        }else{
            if(Sequel.menyimpantf("diagnosa_ranap_draft","?,?,?,?,?","Data",5,new String[]{
                null,TNoRw.getText(),kddok.getText(),cmbJenis.getSelectedItem().toString(),TPemeriksaan.getText()
            })==true){
                tampil();
                emptTeks();
            }   
        }
}//GEN-LAST:event_BtnSimpanActionPerformed

