    public void tampil() {
        Valid.tabelKosong(tabMode);
        try{
            if(TCari.getText().toString().trim().equals("")){
                ps=koneksi.prepareStatement(
                    "select reg_periksa.no_rawat,pasien.no_rkm_medis,pasien.nm_pasien, "+
                    "diagnosa_ranap_draft.kd_dokter,dokter.nm_dokter,diagnosa_ranap_draft.jenis, "+
                    "diagnosa_ranap_draft.deskripsi, diagnosa_ranap_draft.kode "+
                            
                    "from diagnosa_ranap_draft inner join reg_periksa on diagnosa_ranap_draft.no_rawat=reg_periksa.no_rawat "+
                    "inner join pasien on reg_periksa.no_rkm_medis=pasien.no_rkm_medis "+
                    "inner join dokter on diagnosa_ranap_draft.kd_dokter=dokter.kd_dokter order by diagnosa_ranap_draft.kode");
            }else{
                ps=koneksi.prepareStatement(
                    "select reg_periksa.no_rawat,pasien.no_rkm_medis,pasien.nm_pasien, "+
                    "diagnosa_ranap_draft.kd_dokter,dokter.nm_dokter,diagnosa_ranap_draft.jenis, "+
                    "diagnosa_ranap_draft.deskripsi, diagnosa_ranap_draft.kode "+
                            
                    "from diagnosa_ranap_draft inner join reg_periksa on diagnosa_ranap_draft.no_rawat=reg_periksa.no_rawat "+
                    "inner join pasien on reg_periksa.no_rkm_medis=pasien.no_rkm_medis "+
                    "inner join dokter on diagnosa_ranap_draft.kd_dokter=dokter.kd_dokter "+
                    "where diagnosa_ranap_draft.no_rawat like ? or "+
                    "pasien.no_rkm_medis like ? or "+
                    "pasien.nm_pasien like ? or "+
                    "diagnosa_ranap_draft.kd_dokter like ? or "+
                    "dokter.nm_dokter like ? "+
                    "order by diagnosa_ranap_draft.kode ");
            }
                
            try {
                if(TCari.getText().toString().trim().equals("")){
                }else{
                    ps.setString(1,"%"+TCari.getText()+"%");
                    ps.setString(2,"%"+TCari.getText()+"%");
                    ps.setString(3,"%"+TCari.getText()+"%");
                    ps.setString(4,"%"+TCari.getText()+"%");
                    ps.setString(5,"%"+TCari.getText()+"%");
                }
                    
                rs=ps.executeQuery();
                while(rs.next()){
                    tabMode.addRow(new String[]{
                        rs.getString("no_rawat"),rs.getString("no_rkm_medis"),rs.getString("nm_pasien"),
                        rs.getString("kd_dokter"),rs.getString("nm_dokter"),rs.getString("jenis"),
                        rs.getString("deskripsi"),rs.getString("kode")
                    });
                }
            } catch (Exception e) {
                System.out.println("Notif : "+e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }
        }catch(SQLException e){
            System.out.println("Notifikasi : "+e);
        }
        int b=tabMode.getRowCount();
        LCount.setText(""+b);
    }

