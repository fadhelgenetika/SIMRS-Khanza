    private void getData() {
        if(tbObat.getSelectedRow()!= -1){
            TNoRw.setText(tbObat.getValueAt(tbObat.getSelectedRow(),0).toString());
            TNoRM.setText(tbObat.getValueAt(tbObat.getSelectedRow(),1).toString());
            TPasien.setText(tbObat.getValueAt(tbObat.getSelectedRow(),2).toString());
            kddok.setText(tbObat.getValueAt(tbObat.getSelectedRow(),3).toString());
            namadokter.setText(tbObat.getValueAt(tbObat.getSelectedRow(),4).toString());
            cmbJenis.setSelectedItem(tbObat.getValueAt(tbObat.getSelectedRow(),5).toString());
            TPemeriksaan.setText(tbObat.getValueAt(tbObat.getSelectedRow(),6).toString());
        }
    }

